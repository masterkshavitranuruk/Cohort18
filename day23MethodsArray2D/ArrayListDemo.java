package day23MethodsArray2D;

import java.util.ArrayList;

public class ArrayListDemo {

	public static void main(String[] args) {
		ArrayList<Integer> numbers = new ArrayList<Integer>();
		
		numbers.add(3);
		numbers.add(7);
		numbers.add(2);
		for(int a :numbers) {
			System.out.println(a);
		}
		
	}

}
