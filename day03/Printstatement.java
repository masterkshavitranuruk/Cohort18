package day03;

public class Printstatement {

	public static void main(String[] args) {
	//Single-linecomments start with two forward slashs(//).

	//Any text between // and the end of line is ignored by JAVA 
	
	//println--> always prints the information in the new line
	System.out.println(1);
	System.out.println(2);	
	System.out.println(3);	
	
	//print -->prints the information in the same line
	System.out.print(1);
	System.out.print(2);
	System.out.print(3);
	}

}
